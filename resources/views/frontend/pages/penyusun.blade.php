                            melakukan studi LCA. Kerangka ini juga sudah diadopsi dalam SNI ISO 14040:2016 dan SNI ISO
                            14044:2016 tentang manajemen lingkungan. Tahapan-tahapan tersebut diilustrasikan dalam gambar
                            dibawah ini.</p>

