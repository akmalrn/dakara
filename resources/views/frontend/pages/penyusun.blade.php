                        <p><strong>PROPER dan LCA</strong></p>
                        <p>Seperti yang sudah diketahui, kajian AMDAL (Analisis Mengenai Dampak Lingkungan) yang mengarah
                            pada PROPER (Program Penilaian Peringkat Kinerja Perusahaan) sudah biasa digunakan bagi industri
                            di Indonesia. Penilaian pada PROPER ini dilakukan untuk mendorong perusahaan melakukan
                            konservasi terhadap sumber daya dan lingkungan. Beberapa kajian yang dilakukan untuk melakukan
                            PROPER yaitu efisiensi energi listrik, pengendalian emisi udara, pengelolaan limbah B3,
                            efisiensi penggunaan air, beban pencemaran air, keanekaragaman hayati, serta CSR (corporate
                            social responsibility).</p>

                        <p>Penerapan LCA baru dimulai dengan dikeluarkannya PerDirjen PPKL No. P14/PPKL/SET/DIK.0/9/2018.
                            Penetapan ini dilakukan untuk: (i) mendorong perusahaan untuk menaati peraturan
                            perundang-undangan melalui insentif dan disinsentif reputasi, dan (ii) mendorong perusahaan yang
                            sudah baik kinerja lingkungannya untuk menerapkan produksi bersih dan rencananya ke depan untuk
                            melakukan <em>sustainable development</em>. Dengan kata lain, LCA bisa menjadi bentuk
                            penyempurnaan dari AMDAL dalam pengkuantifikasian dampak terhadap lingkungan.</p>

                        <p>Jika dibandingkan dengan PROPER, LCA dapat melakukan penilaian secara menyeluruh dalam proses
                            produksi pada tiap-tiap unit produksi. Dengan melakukan LCA, konsumsi energi maupun air dari
                            tiap-tiap unit produksi dapat diidentifikasi, sehingga pemborosan dapat diketahui secara detail
                            yang bisa berasal dari unit proses tertentu. Dampak terhadap banyak kategori kerusakan
                            lingkungan juga bisa diketahui dengan analisis LCA. Penilaian yang menyeluruh dan mendetail
                            merupakan kelebihan dari LCA itu sendiri.</p>

                        <p><strong>Kegunaan LCA</strong></p>
                        <p>Setelah LCA dilakukan, aliran massa dan energi dari setiap unit proses dapat diketahui secara
                            detail. Dengan indikator ini, pemborosan penggunaan air maupun konsumsi energi listrik dari
                            setiap unit proses bisa diketahui secara detail. Sehingga, optimisasi dapat dirancang pada titik
                            tertentu untuk meningkatkan penghematan dan performa perusahaan. Ini merupakan salah satu
                            kelebihan LCA jika dibandingkan dengan PROPER.</p>

                        <p>Contoh lain yang cukup penting untuk saat ini adalah LCA dapat melakukan penghitungan seberapa
                            besar dampak suatu proses produksi dari produk unggulan perusahaan terhadap lingkungan (misalnya
                            pemanasan global). Optimisasi pengaruhnya terhadap lingkungan dapat direkomendasikan (misalnya
                            desain baru pada suatu unit proses). Hal ini sangat penting untuk meningkatkan EPD
                            (environmental product declaration) ataupun ekolabel sehingga produk perusahaan tersebut dapat
                            bersaing di pasar global. Selain itu, Pustanlinghut dan Biro Hukum KLHK semakin menyempurnakan
                            Peraturan Menteri LHK mengenai Tata Cara Penerapan Label Lingkungan untuk Pengadaan Barang dan
                            Jasa Ramah Lingkungan. Ekolabel dimaksudkan untuk mewujudkan sinergi pengendalian dampak negatif
                            ke lingkungan sepanjang daur hidupnya serta mendorong supply dan demand produk dan jasa ramah
                            lingkungan. Ini merupakan tindak lanjut dari amanat PP No. 46 Tahun 2017 tentang Instrumen
                            Ekonomi Lingkungan Hidup.</p>

