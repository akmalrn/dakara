                        <p><strong>Pengantar</strong></p>

                        <p>Life cycle assessment (LCA) diaplikasikan untuk mengukur dampak suatu proses produksi terhadap
                            lingkungan. Pada kerangka LCA, seluruh tahap dalam daur hidup produk akan diperhatikan. Daur
                            hidup tersebut bisa meliputi: proses pengambilan bahan baku <em>(raw material)</em>, pengolahan
                            bahan hingga produksi, waste yang dihasilkan dari proses produksi, hingga waste yang dihasilkan
                            setelah penggunaan produk tersebut. Batasan dari LCA diilustrasikan oleh gambar di bawah ini.
                        </p>

